#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char *s2;

	s2 = dst;
	while (*src)
	{
		*dst = *src;
		dst++;
		src++;
	}
	*dst = '\0';
	return (s2);
}
