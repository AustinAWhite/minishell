#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*s1;
	size_t	i;

	s1 = (char *)s;
	i = 0;
	while (i < n)
	{
		if (s1[i] == ((char)c))
			return ((void *)s1 + i);
		i++;
	}
	return (NULL);
}
